/**
 * build-workbench-nav.mjs
 *
 * Renders BrandBar with surface='workbench' to static HTML using
 * ReactDOMServer.renderToStaticMarkup, then combines it with nl-nav.css
 * and the runtime interactivity script to produce a self-contained
 * workbench/nav-component.html.
 *
 * Uses Vite's createServer + ssrLoadModule for JSX transformation —
 * no extra dependencies beyond what the project already has.
 *
 * Run:  node scripts/build-workbench-nav.mjs
 */
import { createServer } from 'vite';
import { createElement } from 'react';
import { renderToStaticMarkup } from 'react-dom/server';
import { StaticRouter } from 'react-router-dom/server.js';
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, '..');

function browserRuntimeConfigScript() {
  const firebaseConfig = {
    apiKey: process.env.VITE_FIREBASE_API_KEY || '',
    authDomain: process.env.VITE_FIREBASE_AUTH_DOMAIN || '',
    projectId: process.env.VITE_FIREBASE_PROJECT_ID || '',
    storageBucket: process.env.VITE_FIREBASE_STORAGE_BUCKET || '',
    messagingSenderId: process.env.VITE_FIREBASE_MESSAGING_SENDER_ID || '',
    appId: process.env.VITE_FIREBASE_APP_ID || '',
    measurementId: process.env.VITE_FIREBASE_MEASUREMENT_ID || '',
  };
  const authSessionApiBaseUrl = process.env.VITE_AUTH_SESSION_API_BASE_URL || '';

  return [
    `window.NEWLEAF_FIREBASE_CONFIG = window.NEWLEAF_FIREBASE_CONFIG || ${JSON.stringify(firebaseConfig)};`,
    `window.NEWLEAF_AUTH_SESSION_API_BASE_URL = ${JSON.stringify(authSessionApiBaseUrl)};`,
  ].join('\n');
}

async function main() {
  // Spin up a lightweight Vite server in middleware mode (no HTTP listener)
  // so we can use ssrLoadModule for JSX transformation + import resolution.
  const vite = await createServer({
    root: ROOT,
    server: { middlewareMode: true },
    appType: 'custom',
    logLevel: 'warn',
    ssr: {
      // Let Node handle CJS packages natively instead of Vite transforming them
      external: ['react-router-dom', 'react-router'],
    },
  });

  try {
    // SSR-load BrandBar (Vite handles JSX, path aliases, etc.)
    const { BrandBar } = await vite.ssrLoadModule(
      '/src/shared/components/BrandBar.jsx'
    );

    // Render the workbench nav to static HTML
    const markup = renderToStaticMarkup(
      createElement(
        StaticRouter,
        { location: '/workbench/' },
        createElement(BrandBar, {
          surface: 'workbench',
          authState: 'out',
          access: {
            canAccessApp: (appId) => appId === 'workbench',
            hasRole: () => false,
          },
        })
      )
    );

    // Read CSS, runtime JS, and Firebase auth module
    const brandCss = fs.readFileSync(
      path.join(ROOT, 'src/trading/styles/brand.css'),
      'utf-8'
    );
    const navCss = fs.readFileSync(
      path.join(ROOT, 'src/trading/styles/nl-nav.css'),
      'utf-8'
    );
    const css = `${brandCss}\n\n${navCss}`;
    const modalRuntime = fs.readFileSync(
      path.join(ROOT, 'scripts/workbench-modal-runtime.js'),
      'utf-8'
    );
    const urlRuntime = fs.readFileSync(
      path.join(ROOT, 'scripts/workbench-url-runtime.js'),
      'utf-8'
    );
    const runtime = fs.readFileSync(
      path.join(ROOT, 'scripts/workbench-nav-runtime.js'),
      'utf-8'
    );
    const footerRuntime = fs.readFileSync(
      path.join(ROOT, 'scripts/workbench-footer-runtime.js'),
      'utf-8'
    );
    const authModule = fs.readFileSync(
      path.join(ROOT, 'scripts/workbench-nav-auth.js'),
      'utf-8'
    );

    // Assemble the self-contained nav-component.html
    const output = [
      '<!-- Generated by scripts/build-workbench-nav.mjs — do not hand-edit -->',
      markup,
      '',
      '<style>',
      css,
      '</style>',
      '',
      '<script>',
      browserRuntimeConfigScript(),
      '</script>',
      '',
      '<script>',
      modalRuntime,
      '</script>',
      '',
      '<script>',
      urlRuntime,
      '</script>',
      '',
      '<script>',
      runtime,
      '</script>',
      '',
      '<script>',
      footerRuntime,
      '</script>',
      '',
      '<script type="module">',
      authModule,
      '</script>',
      '',
    ].join('\n');

    const dest = path.join(ROOT, 'workbench/nav-component.html');
    fs.writeFileSync(dest, output);
    console.log(`  \u2713 workbench/nav-component.html generated (${(output.length / 1024).toFixed(1)} KB)`);
  } finally {
    await vite.close();
  }
}

main().catch((err) => {
  console.error('Failed to build workbench nav:', err);
  process.exit(1);
});
